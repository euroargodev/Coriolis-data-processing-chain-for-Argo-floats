% ------------------------------------------------------------------------------
% Compute oxygen partial pressure measurements (PPOX_DOXY) from oxygen sensor
% measurements (MOLAR_DOXY).
%
% SYNTAX :
%  [o_PPOX_DOXY] = compute_PPOX_DOXY_4_19_25( ...
%    a_MOLAR_DOXY, ...
%    a_MOLAR_DOXY_fillValue, ...
%    a_PRES, a_TEMP, ...
%    a_PRES_fillValue, a_TEMP_fillValue, ...
%    a_PPOX_DOXY_fillValue)
%
% INPUT PARAMETERS :
%   a_MOLAR_DOXY           : input MOLAR_DOXY optode data
%   a_MOLAR_DOXY_fillValue : fill value for input MOLAR_DOXY data
%   a_PRES                 : input PRES CTD data
%   a_TEMP                 : input TEMP CTD data
%   a_PRES_fillValue       : fill value for input PRES data
%   a_TEMP_fillValue       : fill value for input TEMP data
%   a_PPOX_DOXY_fillValue  : fill value for output PPOX_DOXY data
%
% OUTPUT PARAMETERS :
%   o_PPOX_DOXY : output PPOX_DOXY data
%
% EXAMPLES :
%
% SEE ALSO :
% AUTHORS  : Jean-Philippe Rannou (Altran)(jean-philippe.rannou@altran.com)
% ------------------------------------------------------------------------------
% RELEASES :
%   06/02/2016 - RNU - creation
% ------------------------------------------------------------------------------
function [o_PPOX_DOXY] = compute_PPOX_DOXY_4_19_25( ...
   a_MOLAR_DOXY, ...
   a_MOLAR_DOXY_fillValue, ...
   a_PRES, a_TEMP, ...
   a_PRES_fillValue, a_TEMP_fillValue, ...
   a_PPOX_DOXY_fillValue)

% output parameters initialization
o_PPOX_DOXY = ones(length(a_MOLAR_DOXY), 1)*a_PPOX_DOXY_fillValue;

% current float WMO number
global g_decArgo_floatNum;

% current cycle number
global g_decArgo_cycleNum;

% arrays to store calibration information
global g_decArgo_calibInfo;

% retrieve global coefficient default values
global g_decArgo_doxy_201and202_201_301_d0;
global g_decArgo_doxy_201and202_201_301_d1;
global g_decArgo_doxy_201and202_201_301_d2;
global g_decArgo_doxy_201and202_201_301_d3;
global g_decArgo_doxy_201and202_201_301_sPreset;
global g_decArgo_doxy_201and202_201_301_b0_aanderaa;
global g_decArgo_doxy_201and202_201_301_b1_aanderaa;
global g_decArgo_doxy_201and202_201_301_b2_aanderaa;
global g_decArgo_doxy_201and202_201_301_b3_aanderaa;
global g_decArgo_doxy_201and202_201_301_c0_aanderaa;
global g_decArgo_doxy_201and202_201_301_b0;
global g_decArgo_doxy_201and202_201_301_b1;
global g_decArgo_doxy_201and202_201_301_b2;
global g_decArgo_doxy_201and202_201_301_b3;
global g_decArgo_doxy_201and202_201_301_c0;
global g_decArgo_doxy_201and202_201_301_pCoef2;
global g_decArgo_doxy_201and202_201_301_pCoef3;


if (isempty(a_MOLAR_DOXY))
   return;
end

% get calibration information
if (isempty(g_decArgo_calibInfo))
   fprintf('WARNING: Float #%d Cycle #%d: DOXY calibration reference salinity is missing\n', ...
      g_decArgo_floatNum, ...
      g_decArgo_cycleNum);
   return;
elseif ((isfield(g_decArgo_calibInfo, 'OPTODE')) && (isfield(g_decArgo_calibInfo.OPTODE, 'DoxyCalibRefSalinity')))
   doxyCalibRefSalinity = g_decArgo_calibInfo.OPTODE.DoxyCalibRefSalinity;
else
   fprintf('WARNING: Float #%d Cycle #%d: inconsistent DOXY calibration information\n', ...
      g_decArgo_floatNum, ...
      g_decArgo_cycleNum);
   return;
end

idDef = find( ...
   (a_MOLAR_DOXY == a_MOLAR_DOXY_fillValue) | ...
   (a_PRES == a_PRES_fillValue) | ...
   (a_TEMP == a_TEMP_fillValue));
idNoDef = setdiff(1:length(a_MOLAR_DOXY), idDef);

if (~isempty(idNoDef))
   
   molarDoxyValues = a_MOLAR_DOXY(idNoDef); % transmitted from float with Sref
   presValues = a_PRES(idNoDef);
   tempValues = a_TEMP(idNoDef);
   psalValues = zeros(size(presValues));
   
   % remove Sref effect
   oxygenSalComp = calcoxy_salcomp_bis(molarDoxyValues, tempValues, psalValues, doxyCalibRefSalinity, ...
      g_decArgo_doxy_201and202_201_301_d0, ...
      g_decArgo_doxy_201and202_201_301_d1, ...
      g_decArgo_doxy_201and202_201_301_d2, ...
      g_decArgo_doxy_201and202_201_301_d3, ...
      g_decArgo_doxy_201and202_201_301_sPreset, ...
      g_decArgo_doxy_201and202_201_301_b0_aanderaa, ...
      g_decArgo_doxy_201and202_201_301_b1_aanderaa, ...
      g_decArgo_doxy_201and202_201_301_b2_aanderaa, ...
      g_decArgo_doxy_201and202_201_301_b3_aanderaa, ...
      g_decArgo_doxy_201and202_201_301_c0_aanderaa, ...
      g_decArgo_doxy_201and202_201_301_b0, ...
      g_decArgo_doxy_201and202_201_301_b1, ...
      g_decArgo_doxy_201and202_201_301_b2, ...
      g_decArgo_doxy_201and202_201_301_b3, ...
      g_decArgo_doxy_201and202_201_301_c0 ...
      );

   % pressure effect correction
   oxygenPresComp = calcoxy_prescomp(oxygenSalComp, presValues, tempValues, ...
      g_decArgo_doxy_201and202_201_301_pCoef2, ...
      g_decArgo_doxy_201and202_201_301_pCoef3 ...
      );

   % compute PPOX_DOXY
   ppoxDoxyValues = O2ctoO2p(oxygenPresComp, tempValues, psalValues, presValues, ...
      g_decArgo_doxy_201and202_201_301_d0, ...
      g_decArgo_doxy_201and202_201_301_d1, ...
      g_decArgo_doxy_201and202_201_301_d2, ...
      g_decArgo_doxy_201and202_201_301_d3, ...
      g_decArgo_doxy_201and202_201_301_b0, ...
      g_decArgo_doxy_201and202_201_301_b1, ...
      g_decArgo_doxy_201and202_201_301_b2, ...
      g_decArgo_doxy_201and202_201_301_b3, ...
      g_decArgo_doxy_201and202_201_301_c0 ...
      );
   
   o_PPOX_DOXY(idNoDef) = ppoxDoxyValues;
end

return;
